public class UnoHand {

	private int cards;
	private int isJocker;

	public int getCards() {
		return this.cards;
	}

	/**
	 * 
	 * @param cards
	 */
	public void setCards(int cards) {
		this.cards = cards;
	}

	public int getIsJocker() {
		return this.isJocker;
	}

	/**
	 * 
	 * @param isJocker
	 */
	public void setIsJocker(int isJocker) {
		this.isJocker = isJocker;
	}

}